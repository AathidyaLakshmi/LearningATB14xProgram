package ex_10_For_Loop;

public class Lab_071_For_If_LOOP {
    public static void main(String[] args) {
        for (int shruthi = 0; shruthi < 18; shruthi++) {
            if (shruthi < 15) {
                System.out.println("She will get iphone as gift");

            } else {
                System.out.println("no gift");
            }
        }
    }
}
//we can have if condition and switch statement and other loop inside for loop
//for 0 to 15 no gift
// for 16 and 17 she got gift