package ex_02_Java_Basics;

public class Lab005_Keyword_Identifier_Demo {
    // public and class are keyword, Lab005_KeywordIdentifierDemo is identifier
    public static void main(String[] args) {
        int age =5;
        //int is a keyword and age is a identifier
        String msg="My age is 5";
        //String is a class name ang msg is a identifier
        if(age==5)
//if is a keyword
        {
            System.out.println(msg);
            //System is a class
            // out and println are identifier
        }
    }

}
