package ex_02_Java_Basics;

public class Lab006_Common_Math {
    public static void main(String[] args) {
        //we can do simple math directly inside sout
        System.out.println(2+2); //prints 4
        System.out.println(2-2);//prints 0
        System.out.println(2*2);//prints 4
        System.out.println(2/2);//prints 1 since quotient 1
        System.out.println(2%2);//prints 0 since remainder 0
        System.out.println(3/2);//prints 1 since quotient 1
        System.out.println(3%2);//prints 1 since remainder 1
    }
}
